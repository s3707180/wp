	<h2>About Us</h2>
    
    <p>Jacinta Le Plastrier is the publisher at JLP. Conor Hutchison is associate publisher.</p> 

   <p>John Leonard is commissioning editor. He taught at Monash and James Cook universities, and is the well-known anthologist of Seven Centuries of Poetry in English (OUP). He has also edited 5 landmark Australian anthologies, including the recent full survey, The Puncher Wattmann Anthology of Australian Poetry, 2009, and Young Poets: An Australian Anthology, 2011. (Several John Leonards inhabit the literary globe. Note that ours is not a poet, and has no other website)</p>

  <p>Visit our <a href="http://www.johnleonardpress.com/media/File/Booklist_June_2013.pdf" target="_blank">booklist</a> for short details of each book including titles with ISBN, price etc.</p>
    <p>For fuller details on each book go to our <a href="http://www.johnleonardpress.com//?id=27211058">catalogue</a> and follow further links.</p>
    <p>Also, you can view <a href="http://www.johnleonardpress.com//?id=59753800">sample poems</a> from the books.</p>
  
  <p>The number of sample poems here from the books is deliberately generous. One normal way of being enticed by a poetry book is to browse it in a bookshop; but unfortunately the number of contemporary Australian poetry titles in many bookshops is a little meagre. These samples are a substitute for such browsing.</p>
