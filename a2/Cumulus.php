	<h5>About Author</h5>
   
    
    <p>Robert Gray was born in 1945 on the north coast of New South Wales. He lives in Sydney, where he has worked in journalism, advertising, as a buyer for bookshops, and more recently as an occasional teacher of literature. Since 1970 he has distributed eight books of verse and six chose releases of his work. For these, he has won the majority of the prizes and artistic honors accessible to a writer in this nation, including the Australia Council's Emeritus Award in 2011. He is additionally the creator of a prize-winning composition diary, The Land I Came Through Last.</p>

<p>When a landscape is sparsely populated, the eye is favored over the ear. Robert Gray spent his childhood in the brilliant coastal spaces of the mid-North Coast of New South Wales, and he has spent his life looking at things, and in searching for the words for them.</p><hr></hr>
