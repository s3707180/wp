	<h2>CUMULUS</h2>    
    
    <h5>About book</h5>
    
    <p>This book is a historic point in Australian verse. For Cumulus, Robert Gray has picked all he wishes to hold from his eight volumes of verse, some of it extensively and fundamentally updated. He has included here another book, 'Anonymous Earth', not already distributed in Australia.</p>

<p>Dim has been a challenging and unique experimenter in the free refrain line, and furthermore on occasion with customary structures. Similarly, his work is striking for its incessant, uncanny rightness in the making of pictures. His reasoning demonstrates a striking familiarity with both Eastern and Western methods of insight (Gray has alluded to himself as a Buddhist apostate). These are all innovator pathways, and this verse arranges them with a clear, traditional temper.</p>

<p>Most striking is an ever-ready promptness—a discernment and brilliance in the liquid minute. Regardless of whether through his exotic dialect or his intense commitment to thoughts, Gray's verse ceaselessly opens us to a new association with the physical world.</p><hr></hr>
