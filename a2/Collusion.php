	<h2>COLLUSION</h2>    
    
    <h5>About book</h5>

<p>Brook Emery’s mode is enquiry, with a gentle insistence that enquiry matters. Fluent, occasionally epigrammatic, and showing a quiet humour, this is generous, open-minded poetry. As in previous collections Emery’s interest is in the intersections of the material, the spiritual and the rational. The poems are loosely addressed as letters to some implied correspondent, who might be real, the self or the unconscious.</p>

 <p>While Collusion is metaphysical in intent, the poems keep up a habit of sharp and tactile observation – the abstract becomes sensuous, and the intellectual makes friends with the physical. Swinging between affirmation and uncertainty, they weigh up the beauty and losses of the natural and human worlds.</p><hr></hr>
