	<h3>REVIEWS</h3>
    
 
<!--REVIEW 2-->
    <h4>From Martin Duwell,<a href="http://www.hotsdots.com/poetry/2013/04/brook-emery-collusion/"> Australian Poetry Review</a>, April 1, 2013: </h4>
    
<p>Brook Emery’s previous book, the excellent Uncommon Light, explored with great subtlety and precision questionss which are usually considered to be the provenance of philosophers of the mind: What is consciousness? How does it relate to the body? What is memory? And a host of other implicated issues: What is thought and how does it relate to meaning? It considered these by bringing to them a poet’s skill, an ability to speak about difficult-to-describe states in a tactile way (while always being aware of the paradoxes of “poetic” methods such as metaphor). The essential movement of the poetry is to undermine whatever solutions or certainties emerge as a result of meditation and when one thinks of Emery’s poetry and the way it is almost always “grounded” at the sea’s edge on the west coast of Australia, it is hard not to think in terms of the way sand shifts continuously beneath our feet, seeming to be both supporting and unstable. This position seems, at first, quite conventional for our time in that it rejects any transcendental ground of being and is highly sensitive to observed processes and interactions but I think it also rejects the Buddhism that might offer it a comfortable home since the virtues of those beliefs and practices are, after all, tied to a baroque theology involving vast imagined cycles of history and processes of rebirth. “That Beat Against the Cage” is a multipart poem from Uncommon Light that hammers away at such issues and its final stanza concludes on a note of dissatisfaction:</p>

<pre>
It’s untenable, this drifting that sees the world as drift.

The fantasy should ebb, become the half-recalled

calling of the sea, or else lifetimes will be spent meandering

self-consciously through the matter of the day,

shuttling back and forth as if transience

could be a domicile, fearful that to stray too far,

stay too long, is to change the story

for an understory, the agreed accepted world

for a thesis of perplexity: a conclusion there is

no evidence to decide, or that the evidence

leads to thoughts the thought cannot sustain.</pre>

<p>I’ve read this as a rejection of Buddhism but it might also be simply a rejection of a poetically convenient way of living in a liminal state, exploiting borders and uncertainties and using uncertainty as a stable ground on which to erect poetic structures full of the gestures that arise from certainty. At any rate, the poem seems to be saying that although uncertainty is a state, it isn’t one to feel comfortable about: transience can’t be a domicile.</p>

<p>But the book isn’t entirely about such matters: woven throughout Uncommon Light were a group of poems addressing a question that usually derives from the philosophical vectors of ethics and religion rather than from those of the nature of consciousness: what is the nature of evil and whence does it come? They weren’t the best poems in the book but their attempts to deal with the issue – significantly they were strongest when they dealt with the poet’s inability to deal with the issue! – were a welcome widening of perspective. This direction isn’t continued in Emery’s new book Collusion, but if it seems to abandon the question of evil it does have some poems about personal guilt.</p>

<p>Above all things, one’s first sense of Collusion is how organised a book it is, how little like a conventional collection of poems. If it keeps a narrower focus than Uncommon Light, it also experiments with a variety of tones, even of types of poems, and places them carefully. The first, last and central poems (they are all untitled) are done in epistolary style, addressed to K. At the moment we think of Kafka and start to explore the possibilities involved in writing to such a figure (or perhaps his protagonist), the middle poem carefully corrects our course:</p>

<pre>
. . . . .

Dear K, I tire of the apparatus of my brain.

I fear that you (my interlocutor, my will,

my conscience) may also tire. The thoughts I think

have passed their use-by dates, are petals tossed

in Burnt Norton’s dusty wind. We could,

we probably do, lead many lives even as

an inoffensive clerk or as a monstrous insect

squirming on its back, feet and feelers wildly

seeking purchase on the air. We stand accused.

We answer allegations we make against ourselves.

                                        *

Someone finding this will think I’m corresponding

with Franz Kafka (it could be Kierkegaard

or Krazy Kat). I’m not that mad, and besides,

Kafka had too many problems of his own (migraines, boils,

constipation, tuberculosis, a certain paranoia). . . .</pre>

<p>Although this invokes Eliot (twice) as well as Kafka, a book containing poems as imaginary letters, or letters to imaginary recipients (“corresponding” is an interesting pun) recalls the work of Bruce Beaver, especially his Letters to Live Poets, and reminds one that that poet, too, was an inhabitant of a Sydney beach surrounded by an environment which both thrust particulars at you while at the same time reminding you of their essential instability all in a sharp, crystal clear light. I can’t remember any earlier poems by Emery which are homages to Beaver but one of the groups of poems which are carefully interspersed throughout Collusion are clearly done in one of Beaver’s styles, probably that of the “Days” sequence of Odes and Days, the third of Beaver’s great central triptych of books. I’ll quote the first of them in full (it’s the fifth poem of Collusion):</p>

<pre>
It’s almost spring in our neglected hemisphere.

As yet no indication we’ve tilted far enough

to receive the annual, waited-for reward.

The sea and sky volley what there is of dusk

and a peevish wind plays nip and tuck

to irritate the waves. In its own good time

the sun will be here and the sea all aquamarine

as if, overnight, spirit could manifest as light

and just this startling colour. Then morning warmth,

leaves on imported trees, poems (God help us!),

and mothballs for our heavy winter clothes.

And are we lighter too. Do we deserve it?

No. But the punishing and forgiving world

will give it to us anyway and I’ll give thanks

though to whom or what it’s useless to inquire.</pre>

<p>This is such a good approximation of a Beaver poem that it could actually be one and if I had had my Beaver collections at hand while writing this I would have nervously checked through them to make sure that it isn’t a quotation, perhaps from a late book like The Long Game. At any rate it catches the Beaver tone perfectly with its sudden unusual perspective (“our neglected hemisphere”), its sense of the world as a place to be lamented and celebrated, its tremendous drive that spills across into (and weirdly animates) a bathetic conclusion. The only thing that doesn’t seem Beaverish is the pun on “lighter” in the twelfth line. There are another six poems in this mode. If I had to guess the impetus behind them I would say that they experiment with inhabiting Beaver’s approach to living in the world. They temporarily eschew the elegant and subtle exploration of mind, thought and the real (and the balanced states of their inter-relationship) which mark most of the Emery poems, for an attitude of sudden brusque involvement resulting in a short, sharp lyric poem but one in which wider perspectives are included, not in a solemn, gestural way (as though a profundity were being offered, gift-wrapped, to the reader) but in a casual, tossed-off one.</p>

<p>There is another group of poems spaced through the book which identify themselves not only in that they are all ten lines long but in that all begin with ellipsis points and an indented first line – a clear indication that these are to be read as snapshots of process, though they might also be rescued fragments of one single long poem. The first two are memorable for their presentation of differing but equally symbolic scenes. In the first the author and (presumably) partner are placed between “the receding arcs of sea and sky” in front and “the green and terrible forest” behind. The two exist, of course, on the liminal sand (described here, with a nice example of that distinctive kind of pun which I think is called paronomasia, as “the intervening sleight of sand”) but they aren’t static: “our feet / lifted and set down, lifted and set down . . .”. In the second poem, examples of hard-nosed industry “three men in hardhats / and orange coveralls” on a bridge (already established in the book’s first poem as being in opposition to the flowing element beneath) are contrasted with a mannequin “forty feet below in a pink gown / and imitation pearls”.</p>

<p>The other poems of Collusion continue to recall Beaver in that they seem to be diary-like meditations, occasioned by living in the world: “All morning it’s been difficult to settle, difficult to harness / energy or purpose for all the things / I have to do.” Their distinctive movement is to be strung between relentless denial and tentative affirmation. A couple of them describe dreams and three, late in the book, deal with memories. One of these latter is prompted by a bicycle ride (and contains the clause “We can’t go back” which is surely an allusion to Beaver’s novel) another by an old photo and the third by recurrent domestic guilts induced by the humming monotony of an aeroplane flight. Compared with the issue of the monstrous evils explored in Uncommon Light, these guilts seem very minor: burying a younger brother up to his neck in the back yard, losing him at the Show, having a near disaster with his children in the surf. As the poem’s last stanza says: “This light-weight guilt is carried on the wind, along with doubt, / longing, nothing more than dust, clouds, rain, squall after squall, / as if wind intended to drag the whole Antarctic north . . .” But despite visits to the worlds of dream and guilt, these poems seem, essentially metaphysical in their obsessions.</p>


<pre>
One late poem works hard to describe a state of what might be called “significance”, experienced physically:

I almost understand this resonance, this hum

or echo which I can only picture as a frequency,

oscillations expanding and diminishing

from a single source. And the sometime static

which crackles and interrupts, which implies

another source, another thought or possibility. . . .</pre>

<p>There is a central statement, “It’s not persistent but too here and now / to be dismissed as fleeting”, and then life returns to the commonplace – a grandchild sleeps in the back of the car and the poet reads Mark Strand. Fittingly, exactly as many stanzas are devoted to the everyday as to the definition of the barely describable state.</p>

<p>And this state, or something like it, is familiar from many of Emery’s poems. In one of them it appears at dawn in hypnagogic and liminal guise and demands consideration despite the cruder intrusion of early-morning sexual desire: “No. Not here. Not now. There’s so much to consider. The sequence of sounds, the unknowable and what it means, the time it takes // to cross an interval between two spots or states . . .” One of the best poems is an extended attempt at description culminating in metaphors deployed as expression of both difference and similarity:</p>

<pre>
. . . . .

My mind is silent too

and still. I can’t describe it. Not empty

like some vessel, not grey and wispy

like a fog: something more substantial,

not set and settled but curiously serene,

like breathing starlight . . .</pre>

<p>Perhaps, ultimately, a metaphor like this final one is the most powerfully descriptive mode though it is hedged about with problems.</p>

<p>Above all, throughout Emery’s poetry and repeatedly here in Collusion, there is a refusal to locate in this state some kind of transcendental ground. There is also a refusal of the next level of stasis whereby the refusal to accept a transcendental ground becomes a ground in itself. There isn’t any celebration of uncertainty here, more a process of living attuned to what is happening as one’s mind engages the manifold dimensions of reality. As the first poem in the book says:</p>

<pre>
. . . . .

The glimmerings are flecks of time.

I can’t decide whether they are truly in the moment or

moments out of time, essence or deviation from the path.

There’s no conclusion here, no resolution myth. Things rise up

and fall away as if they never were, rise up again. I like the

dancing light,

the scattered cloud, the river that lies potentially between its banks,

the speeding train. I reach for them. They reach for me.</pre>
    
