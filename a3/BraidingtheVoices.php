													<p>Peter Steele, who was born in Perth in 1939, moved to Melbourne in 1957 to join the Jesuits, of whom he remains a member. He is Professor Emeritus at the University of Melbourne, his base for a lifetime of teaching and writing, along with the equivalent of several years at universities in Chicago, New York and Washington DC. Alongside numerous books of poetry, including ‘The Gossip and the Wine’ (JLP, 2010) and ‘White Knight with Beebox: New and Selected Poems’ (JLP, 2008),  he has published books of literary criticism, including studies of Jonathan Swift and Peter Porter.</p>
